package actuarical.model;

public class ModelInfo {
	//模型名称
	private String modelName = "" ;
	//模型类别
	private String modelCategory = "" ;
	//模型公式
	private String modelFormula = "" ;
	//模型输入
	private String modelInput = "" ;
	//模型输出
	private String modelOutput = "" ;
	//模型说明
	private String modelExplain = "" ;
	public String getModelName() {
		return modelName;
	}
	public void setModelName(String modelName) {
		this.modelName = modelName;
	}
	public String getModelCategory() {
		return modelCategory;
	}
	public void setModelCategory(String modelCategory) {
		this.modelCategory = modelCategory;
	}
	public String getModelFormula() {
		return modelFormula;
	}
	public void setModelFormula(String modelFormula) {
		this.modelFormula = modelFormula;
	}
	public String getModelInput() {
		return modelInput;
	}
	public void setModelInput(String modelInput) {
		this.modelInput = modelInput;
	}
	public String getModelOutput() {
		return modelOutput;
	}
	public void setModelOutput(String modelOutput) {
		this.modelOutput = modelOutput;
	}
	public String getModelExplain() {
		return modelExplain;
	}
	public void setModelExplain(String modelExplain) {
		this.modelExplain = modelExplain;
	}
	
}
